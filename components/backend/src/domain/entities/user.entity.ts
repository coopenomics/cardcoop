/**
 * Сущность пользователя.
 *
 * Основная доменная сущность, представляющая пользователя в системе.
 * Содержит только необходимые данные для идентификации и аутентификации
 * пользователя в системе.
 */
export class User {
  /**
   * Уникальный идентификатор пользователя
   */
  id!: string;

  /**
   * Email пользователя, используется для входа и восстановления доступа
   */
  email!: string;

  /**
   * Соль для хеширования ключа пользователя
   */
  salt!: string;

  /**
   * Хешированный ключ пользователя (используется вместо пароля)
   */
  hash_key!: string;

  /**
   * Создает экземпляр пользователя.
   *
   * @param data - Данные пользователя
   */
  constructor(data: User) {
    Object.assign(this, data);
  }
}
